function makeSymbol(t: [string, string, string], segment: 'EQ' | 'FO' = 'EQ'): Symbol {
  return {
    symbol: t[0],
    name: t[1],
    exchange: 'NSE',
    segment,
    isin: 'INE' + (Math.abs(hash(t[0])) % 1e10).toString().padStart(10, '0').slice(0, 10),
    lotSize: 1,
    tickSize: 0.05,
    sector: t[2],
  }
}

function hash(s: string): number {
  let h = 0
  for (let i = 0; i < s.length; i++) h = ((h << 5) - h + s.charCodeAt(i)) | 0
  return h
}

